CREATE DEFINER=`root`@`localhost` PROCEDURE `cek_iuran_global_for_wa` (IN `p_anggota_id` INT)   BEGIN
    WITH RECURSIVE numbers AS (
        SELECT 0 AS n
        UNION ALL
        SELECT n + 1 FROM numbers WHERE n < 35
    ),
    calendar AS (
        SELECT 
            a.id AS anggota_id,
            DATE_ADD(DATE(a.bergabung_sejak), INTERVAL n MONTH) AS periode
        FROM anggota a
        CROSS JOIN numbers
        WHERE p_anggota_id IS NULL OR a.id = p_anggota_id
    )
    SELECT
        a.id AS anggota_id,
        a.nama_lengkap,
        a.no_hp,
        DATE_FORMAT(c.periode, '%Y') AS tahun,
        c.periode,
        COALESCE(SUM(i.jumlah_bayar), 0) AS total_bayar
    FROM anggota a
    JOIN calendar c ON a.id = c.anggota_id
    LEFT JOIN iuran i
        ON i.anggota_id = a.id
        AND YEAR(i.periode) = YEAR(c.periode)
        AND MONTH(i.periode) = MONTH(c.periode)
    WHERE a.no_hp IS NOT NULL AND a.no_hp != ''
      AND c.periode <= CURDATE()
    GROUP BY a.id, a.nama_lengkap, a.no_hp, c.periode
    ORDER BY a.id, c.periode;
END$$

CREATE DEFINER=`root`@`localhost` PROCEDURE `rekap_iuran_tahunan_for_wa` (IN `p_tahun` INT)   BEGIN
    SELECT
        a.id AS anggota_id,
        a.nama_lengkap,
        a.no_hp,
        COALESCE(SUM(i.jumlah_bayar), 0) AS total_bayar,
        (
            GREATEST(0,
                TIMESTAMPDIFF(
                    MONTH,
                    GREATEST(MAKEDATE(p_tahun,1), COALESCE(a.bergabung_sejak, MAKEDATE(p_tahun,1))),
                    MAKEDATE(p_tahun,12)
                ) + 1
            ) * 10000
        ) AS total_seharusnya,
        CASE
            WHEN COALESCE(SUM(i.jumlah_bayar), 0) = 0 THEN 'Belum Bayar'
            WHEN COALESCE(SUM(i.jumlah_bayar), 0) <
                (
                    GREATEST(0,
                        TIMESTAMPDIFF(
                            MONTH,
                            GREATEST(MAKEDATE(p_tahun,1), COALESCE(a.bergabung_sejak, MAKEDATE(p_tahun,1))),
                            MAKEDATE(p_tahun,12)
                        ) + 1
                    ) * 10000
                ) THEN 'Kurang'
            ELSE 'Lunas'
        END AS status_tahunan
    FROM anggota a
    LEFT JOIN iuran i
        ON i.anggota_id = a.id
        AND YEAR(i.periode) = p_tahun
    WHERE a.no_hp IS NOT NULL AND a.no_hp != ''
    GROUP BY a.id, a.nama_lengkap, a.no_hp
    HAVING status_tahunan IN ('Kurang', 'Belum Bayar')
    ORDER BY a.id;
END$$

